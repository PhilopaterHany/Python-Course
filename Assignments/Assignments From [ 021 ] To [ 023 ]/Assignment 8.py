friends = ["Ahmed", "Osama", "Philopater", "Farah", "Ramy", "Sarah"]

friends.sort()
print(friends)  # Output => ["Ahmed", "Farah", "Osama", "Philopater", "Ramy", "Sarah"]

print("=" * 50)  # Separator

friends.sort(reverse=True)
print(friends)  # Output => ["Sarah", "Ramy", "Philopater", "Osama", "Farah", "Ahmed"]
