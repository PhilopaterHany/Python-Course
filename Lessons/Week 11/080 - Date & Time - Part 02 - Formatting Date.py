# ------------------------------------
# -- Date & Time => Formatting Date --
# ------------------------------------
# https://strftime.org/
# ------------------------------------

import datetime

myBirthday = datetime.datetime(2005, 8, 4)

print(myBirthday)
print(myBirthday.strftime("%a"))
print(myBirthday.strftime("%A"))
print(myBirthday.strftime("%b"))
print(myBirthday.strftime("%B"))

print("=" * 50)  # Separator

print(myBirthday.strftime("%d %B %Y"))
print(myBirthday.strftime("%d, %B, %Y"))
print(myBirthday.strftime("%d/%B/%Y"))
print(myBirthday.strftime("%d - %B - %Y"))
print(myBirthday.strftime("%B - %Y"))
