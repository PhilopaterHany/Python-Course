# --------------------------
# -- Arithmetic Operators --
# --------------------------
# [+]   => Addition
# [-]   => Subtraction
# [*]   => Multiplication
# [/]   => Division
# [%]   => Modulus
# [**]  => Exponent (Power)
# [//]  => Floor Division
# --------------------------

# Addition
print(10 + 30)  # 40
print(-10 + 20)  # 10
print(1 + 2.66)  # 3.66
print(1.2 + 1.2)  # 2.4

print("=" * 50)  # Separator

# Subtraction
print(60 - 30)  # 30
print(-30 - 20)  # -50
print(-30 - -20)  # -10
print(5.66 - 3.44)  # 2.22

print("=" * 50)  # Separator

# Multiplication
print(10 * 3)  # 30
print(5 + 10 * 100)  # 1005
print((5 + 10) * 100)  # 1500

print("=" * 50)  # Separator

# Division
print(100 / 20)  # 5.0
print(int(100 / 20))  # 5

print("=" * 50)  # Separator

# Modulus
print(8 % 2)  # 0
print(9 % 2)  # 1
print(20 % 5)  # 0
print(22 % 5)  # 2

print("=" * 50)  # Separator

# Exponent (Power)
print(2 ** 5)  # 32
print(2 * 2 * 2 * 2 * 2)  # 32
print(5 ** 4)  # 625
print(5 * 5 * 5 * 5)  # 625
print("=" * 50)  # Separator

# Floor Division
print(100 // 20)  # 5
print(119 // 20)  # 5
print(120 // 20)  # 6
print(140 // 20)  # 7
print(142 // 20)  # 7
