# ----------------------------------
# ------ Comparison Operators ------
# ----------------------------------
# [ == ]    => Equal
# [ != ]    =>  Not Equal
# [ > ]     => Greater Than
# [ < ]     => Less Than
# [ >= ]    => Greater Than Or Equal
# [ <= ]    => Less Than Or Equal
# ----------------------------------

# Equal + Not Equal
print(100 == 100)
print(100 == 200)
print(100 == 100.00)

print("=" * 50)  # Separator

print(100 != 100)
print(100 != 200)
print(100 != 100.00)

print("=" * 50)  # Separator

# Greater Than + Less Than
print(100 > 100)
print(100 > 200)
print(100 > 100.00)
print(100 > 40)

print("=" * 50)  # Separator

print(100 < 100)
print(100 < 200)
print(100 < 100.00)
print(100 < 40)

print("=" * 50)  # Separator

# Greater Than Or Equal + Less Than Or Equal
print(100 >= 100)
print(100 >= 200)
print(100 >= 100.00)
print(100 >= 40)

print("=" * 50)  # Separator

print(100 <= 100)
print(100 <= 200)
print(100 <= 100.00)
print(100 <= 40)

print("=" * 50)  # Separator
